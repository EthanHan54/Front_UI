package com.example.sub_ui_mk1;

public class PolarInfo {
    public int code;
    public String place;
    public String text;
    public int imagefood;
    public String main;
    public String love;
    public String date;

    public PolarInfo(int code, String place, String text, int imagefood, String main, String love, String date) {
        this.code = code;
        this.place = place;
        this.text = text;
        this.imagefood = imagefood;
        this.main = main;
        this.love = love;
        this.date = date;
    }
}
